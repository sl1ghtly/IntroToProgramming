public class CountStars2
{
   public static void main(String [] args)
   {
   // for loop
   for (int outer = 1; outer <= 3; outer++)
   {
      for (int inner = 1; inner <= 10; inner++)
      {
         System.out.print("*");
      }
   System.out.println();
   }
   }
}